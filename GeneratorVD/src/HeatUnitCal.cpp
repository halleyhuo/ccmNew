ushort CGenHeatCal::findColdforSecond(ulong power)//
{
	if(power>coldList[0])
		return 0;
	if(power<coldList[timerMax-1])
		return timerMax-1;
	for(int i=0;i<(timerMax-1);i++)
	{
		if((power<=coldList[i])&&(power>=coldList[i+1]))
		{
			if((coldList[i]-power)>=(coldList[i+1]-power))
			{
				return i+1;
			}
			else
			{
				return i;
			}
		}
	}
	return timerMax-1;
}
ushort CGenHeatCal::costTimeCount(ulong power)
{
	if(power>coldList[0])
		return timerMax;
	if(power<coldList[timerMax-1])
		return 1;
	for(int i=0;i<(timerMax-1);i++)
	{
		if((power<=coldList[i])&&(power>=coldList[i+1]))
		{
			if((coldList[i]-power)>=(coldList[i+1]-power))
			{
				return timerMax-i-1;
			}
			else
			{
				return timerMax-i;
			}
		}
	}
	return 1;
}
void CGenHeatCal::HeateCalInSecond(void* param)
{
	if(param!=NULL)
	{

		CGenHeatCal* threadPointer = (CGenHeatCal*)param;
		pthread_mutex_lock(&tHeatCalMutex);
		heatData.waittime = costTimeCount(heatData.current);
		ushort index = findColdforSecond(heatData.oldpower);
		if(index <= timerMax-1-10)//10
			heatData.colder = coldList[index]-coldList[index+10];//1
		else
			heatData.colder = coldList[index];//0
		if(heatData.current>= heatData.colder)
			heatData.current -= heatData.colder;
		else
			heatData.current = 0;		
		heatData.percentage = heatData.current*10/heatData.HeatMax;//
		heatData.oldpower = heatData.current;
		pthread_mutex_unlock(&tHeatCalMutex);
		iTubeLoad tmp;
		tmp.tubeload = heatData.percentage;
		tmp.waittime = heatData.waittime;
		CGenXtpIf::Sigleton()->onSendTubeLoad(&tmp);
		IDC::Log(IDC::LOG_NOTICE,LOG_TAG,"current is %d",heatData.current);
	}
}

